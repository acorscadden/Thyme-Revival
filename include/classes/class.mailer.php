    /**
     * Creates message and assigns Mailer. If the message is
     * not sent successfully then it returns false.  Use the ErrorInfo
     * variable to view description of the error.  
     * @return bool
     */
    function Send($header,$body,$subject) {

        for($i = 0; $i < count($this->to); $i++)
        {  
           if($i != 0) { $to .= ", "; }
           $to .= $this->to[$i][0];
        }

        switch(intval(_CAL_MAIL_PROG_))
        {
           case 0:
              $mail_prog = 'mail';
              $this->Subject = $subject;
              break;

           case 1:
              $mail_prog = 'sendmail';
	      $header = "To: ". $to ."\n" . $header;
	      $header = "Subject: ". $subject ."\n" . $header;
              break;

           case 2:
              $mail_prog = 'smtp';
	      $header = "To: ". $to ."\n" . $header;
              $header = "Subject: ". $subject ."\n" . $header;
              break;
        }

        $this->Mailer   = $mail_prog;

        # SET MAILER VALUES
        #######################
        if ($mail_prog == 'smtp' ) {
                $this->SMTPAuth = (@constant("_CAL_MAIL_AUTH_") == "yes");
                $this->Username = @constant("_CAL_MAIL_AUTH_USER_");
                $this->Password = @constant("_CAL_MAIL_AUTH_PASS_");
                $this->Host     = _CAL_MAIL_SERVER_;
                $this->Port = (@constant("_CAL_MAIL_PORT_") ? _CAL_MAIL_PORT_ : 25);

        } else if ( $mail_prog == 'sendmail' ) {
             $this->Sendmail = _CAL_MAIL_PATH_;
        } // if


        // Choose the mailer
        switch($this->Mailer)
        {
            case "sendmail":
                $result = $this->SendmailSend($header, $body);
                break;
            case "mail":
                $result = $this->MailSend($header, $body);
                break;
            case "smtp":
                $result = $this->SmtpSend($header, $body);
                break;
            default:
            $this->SetError($this->Mailer . $this->Lang("mailer_not_supported"));
                $result = false;
                break;
        }

        return $result;
    }

